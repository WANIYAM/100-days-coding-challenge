/*                                      QUESTION 75
Compound Assignment Operators: Use compound assignment operators to simplify arithmetic operations. 
Start with x = 4 and performa series of operations (addition, subtraction, multiplication, division) 
on x using compound operators.
*/

let x = 10;

x += 2;   // Addition: x = x + 2
console.log(x);  

x -= 1;   // Subtraction: x = x - 1
console.log(x);  

x *= 3;   // Multiplication: x = x * 3
console.log(x);  

x /= 5;   // Division: x = x / 5
console.log(x);  
