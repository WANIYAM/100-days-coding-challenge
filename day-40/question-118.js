/*                         QUESTION 118
Write a loop that logs numbers from 1 to 10 to the console.
*/
// Function to log numbers from 1 to 10 to the console
function logNumbers() {
    // Use a for loop to log numbers from 1 to 10
    for (var i = 1; i <= 10; i++) {
        console.log(i);
    }
}
logNumbers();
